#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	long	j;

	i = 1;
	j = 0;
	while (*str == '\t' || *str == '\n' || *str == '\v' || *str == '\f' \
			|| *str == '\r' || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
		if (*str++ == '-')
			i = -1;
	while (*str >= '0' && *str <= '9')
		j = j * 10 + (*str++ - '0');
	return (j * i);
}
