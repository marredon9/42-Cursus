#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	if (!s)
	{
		write (1, "(null)", 6);
		return (6);
	}
	i = 0;
	while (*s)
	{
		ft_putchar(*s++);
		i ++;
	}
	return (i);
}
